package Day1;

public class Swapping {


    public static void main(String[] args)
    {

        //Approach 1
        double a=10;
        double b=20;
        System.out.println("Before swapping tha value of a and b is :"+" "+a+" "+b);

        a+=10;
        b-=10;

        System.out.println("After swapping the value of a and b is: " +a+" "+b);

        //Approach 2
        a=10;
        b=20;

        System.out.println("Before swapping tha value of a and b is :"+" "+a+" "+b);

        a*=2;
        b*=0.5;

        System.out.println("After swapping the value of a and b is: " +a+" "+b);






    }


}
